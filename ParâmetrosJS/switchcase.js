// -SWITCH CASE SIM... MEU PESADELO- //

let dia = 3;

switch (dia) {
  case 1:
    console.log("Hoje é DOMINGO");
    break;

  case 2:
    console.log("Hoje é SEGUNDA");
    break;

  case 3:
    console.log("Hoje é TERÇA");
    break;

  default:
    console.log("ERRO do PROGRAMA");
}

// -NOTA- //

let nota = "B";

switch (nota) {
  case "A":
    console.log("Passou com a nota A");
    break;

  case "B":
    console.log("Passou com a nota B");
    break;

  case "C":
    console.log("Passou com a nota C");
    break;

  default:
    console.log("REPROVADO!!! KKKKKKK");
}

// -UTILIZAMOS TRUE CASO SEJA VERDADEIRO- //

let numero = 5;

switch (true) {
  case numero < 5:
    console.log("Menor que 5");
    break;

  case numero === 5:
    console.log("Igual 5");
    break;

  default:
    console.log("ERRO KKK.");
}
